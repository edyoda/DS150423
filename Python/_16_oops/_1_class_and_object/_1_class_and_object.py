# class  - is the blueprint (planning)
# object - it is the instance of the class

# methods - functions defined inside the class are known as methods

class building:          # class
    
    def bricks(self):    # method
        print("1000 Bricks")

    def doors(self):
        print("20 doors")

obj = building()          # object
obj.bricks()              # calling method via object
obj.doors()


